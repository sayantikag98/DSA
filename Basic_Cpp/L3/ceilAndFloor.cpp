#include<iostream>
#include<cmath>
using namespace std;

int main(){
	cout<<ceil(-6)<<" "<<floor(-6)<<endl;
	cout<<ceil(-6.6)<<" "<<floor(-6.6)<<endl;
	cout<<ceil(-6.3)<<" "<<floor(-6.3)<<endl;
	cout<<ceil(-6.5)<<" "<<floor(-6.5)<<endl;
	cout<<ceil(6)<<" "<<floor(6)<<endl;
	cout<<ceil(6.3)<<" "<<floor(6.3)<<endl;
	cout<<ceil(6.6)<<" "<<floor(6.6)<<endl;
	cout<<ceil(6.5)<<" "<<floor(6.5)<<endl;
}


/*

All the numbers with a decimal point will have cel and floor value different
otherwise numbers without a decimal point will have cel and floor value same

*/